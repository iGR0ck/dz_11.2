def  upper_str (text_str):
    """
    Делает заглавными все буквы в словах
    :param text_str: введённое предложение
    :return: строка написанная заглавными буквами
    """
    return text_str.upper()

def  title_str (text_str):
    """
    Делает заглавными первые буквы слов
    :param text_str: введённое предложение
    :return: строка с озаглавленными словами
    """
    return text_str.title()